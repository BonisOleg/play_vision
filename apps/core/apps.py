from django.apps import AppConfig


class CoreConfig(AppConfig):
    default_auto_field = 'django.db.models.BigAutoField'
    name = 'apps.core'
    verbose_name = 'Core'
    
    def ready(self):
        """Import signal handlers when app is ready"""
        try:
            from . import signals
            signals.setup_signals()
        except ImportError:
            pass